package com.example.spring.model;

public class DisplayClass {
	private String name;

	public DisplayClass() {
		super();
	}

	public DisplayClass(String name) {
		super();
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	

}
